sayi1 = int(input("Bir sayı giriniz ="))
sayi2 = int(input("Bir sayı daha giriniz ="))

print("{0}+{1} = {2}".format(sayi1,sayi2,sayi1+sayi2))
print("{0}-{1} = {2}".format(sayi1,sayi2,sayi1-sayi2))
print("{0}*{1} = {2}".format(sayi1,sayi2,sayi1*sayi2))
print("{0}/{1} = {2}".format(sayi1,sayi2,sayi1/sayi2))

